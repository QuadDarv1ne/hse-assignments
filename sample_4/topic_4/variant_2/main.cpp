/*
    Основная функция: Интеграция обеих задач.

    Объяснение основной функции:
    Шаг №1: Мы читаем числа из файла с помощью функции readFile.
    Шаг №2: Вызываем функцию countUniqueNumbers, чтобы подсчитать уникальные числа и их количество.
    Шаг №3: Вычисляем математический размах чисел с помощью функции findRange и выводим результат.
*/

#include <iostream>
#include <fstream>
#include <vector>
#include <map>
#include <algorithm>

using namespace std;

int main() {
    const string filename = "numbers.txt";

    // Шаг 1: Чтение чисел из файла
    vector<int> numbers = readFile(filename);
    if (numbers.empty()) {
        cerr << "Ошибка: Не удалось прочитать числа из файла.\n";
        return 1;
    }

    // Шаг 2: Подсчёт уникальных чисел и их вхождений
    countUniqueNumbers(numbers);

    // Шаг 3: Нахождение математического размаха чисел
    int range = findRange(numbers);
    cout << "Математический размах чисел: " << range << "\n";

    return 0;
}

/*
    Резюме:
    - Подсчёт уникальных чисел: Мы используем std::map для подсчёта, сколько раз каждое число встречается в файле.
    - Математический размах: Размах чисел вычисляется как разница между максимальным и минимальным значением в наборе данных.
*/